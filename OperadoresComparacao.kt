fun main(args: Array<String>) {

    var A = 10
    var B = 20

    println(A > B) // A é maior que B? Não, false
    println(A < B) // B é maior que A? Sim, true
    println(A == B) // A é igual a B? Não, false
    println(A != B) // A é diferente de B? Sim, true
    println(A >= B) // A é maior ou igual a B? Não, false
    println(A <= B) // A é menor ou igual a B? Sim, true

}